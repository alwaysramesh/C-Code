#include <iostream>
using namespace std;
int main()
{   //Arithmetic Operators
    int x,y;
    cout<<"Enter a value of x: ";
    cin>>x;
    cout<<"Enter a value of y: ";
    cin>>y;
    int add,sub,mul,div;
    add = x+y;
    sub = x-y;
    mul = x*y;
    div = x/y;
    cout<<"Ans of addition: "<<add<<endl;
    cout<<"Ans of subtraction: "<<sub<<endl;
    cout<<"Ans of multiplication: "<<mul<<endl;
    cout<<"Ans of division: "<<div<<endl;
     

  return 0;
}
